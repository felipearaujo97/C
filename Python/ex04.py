# Calcular e exibir a média aritmética de quatro valores quaisquer que serão digitados.

val1 = float(input("Digite o valor 1: "))
val2 = float(input("Digite o valor 2: "))
val3 = float(input("Digite o valor 3: "))
val4 = float(input("Digite o valor 4: "))
 
m = (val1+val2+val3+val4)/4
 
print(f"A média dos valores e {m:.2f}")